def addition_number(number_1, number_2):
    summa = number_1 + number_2
    return summa


def search_max_number_in_list(sp):
    max_number = max(sp)
    return max_number


def output_name(name):
    print(f'Приветствую,{name}')


print(addition_number(1, 2))
print(search_max_number_in_list([1, 5, 9, 48]))
